package Lab7;

public class Point2d {
	private double x;
	private double y;
	
	public Point2d(double x, double y) {
		this.x = x;
		this.y = y;
	}
	
	public double getX() {
		return x;
	}
	
	public double getY() {
		return y;
	}
	
	public void setX(double xNew) {
		x = xNew;
	}
	
	public void setY(double yNew) {
		y = yNew;
	}
	
}